    public void ProcessChkKey(java.awt.event.KeyEvent evt) {
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            bntExitClick();
        }
        if (evt.getKeyCode() == KeyEvent.VK_F5) {
            bntOKClick();
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            if (txtGroup1.hasFocus()) {
                txtGroup2.requestFocus();
            }
            if (txtGroup2.hasFocus()) {
                txtPlu1.requestFocus();
            }
            if (txtPlu1.hasFocus()) {
                txtPlu2.requestFocus();
            }
            if (txtPlu2.hasFocus()) {
                txtGroup1.requestFocus();
            }
        }
    }
    public void bntOKClick() {
        String Group1 = txtGroup1.getText();
        String Group2 = txtGroup2.getText();
        String Plu1 = txtPlu1.getText();
        String Plu2 = txtPlu2.getText();

        String TempGroup = "";
        String TempPlu = "";
        String TempName = "";
        int ArraySize = 0;
        Boolean Found = false;
        PluRec[] GArray;
        GArray = new PluRec[1];
        ArraySize = 0;
        
        try {
            Statement stmt =  MySQLConnect.con.createStatement();
            String SqlQuery = "select *from t_charge left join billnocharge on b_refno=r_refno where "+
                    "(r_group>='" + Group1 + "') and (r_group<='" + Group2 + "') " +
                    "and (r_plucode>='"+Plu1 +"') and (r_plucode<='"+Plu2+"') " +
                    "and (r_void<>'V') and (r_refund<>'V') Order by r_group,r_plucode";
//            System.out.println(SqlQuery);
            ResultSet rec = stmt.executeQuery(SqlQuery);
            rec.first();
            TempGroup = "";
            TempPlu = "";
            TempName = "";
            Double SumEQty = 0.0;
            Double SumEAmt = 0.0;
            Double SumTQty = 0.0;
            Double SumTAmt = 0.0;
            Double SumSQty = 0.0;
            PluRec GroupRec = new PluRec();
            GroupRec.MacNo1 = "";
            GroupRec.MacNo2 = "";
            GroupRec.Cashier1 = "";
            GroupRec.Cashier2 = "";
            GroupRec.Group1 = Group1;
            GroupRec.Group2 = Group1;
            GroupRec.Plu1 = Plu1;
            GroupRec.Plu2 = Plu2;
            GroupRec.GroupCode = TempGroup;
            GroupRec.GroupName = PUtility.SeekGroupName(TempGroup);
            GroupRec.PCode = TempPlu;
            GroupRec.PName = TempName;

            GroupRec.E_Qty = GroupRec.E_Qty + SumEQty;
            GroupRec.E_Amt = GroupRec.E_Amt + SumEAmt;


            GroupRec.T_Qty = GroupRec.T_Qty + SumTQty;
            GroupRec.T_Amt = GroupRec.T_Amt + SumTAmt;

            GroupRec.S_Qty = GroupRec.S_Qty + SumSQty;
            GArray[0] = GroupRec;
            if (rec.getRow() == 0) {
            } else {
                TempGroup = rec.getString("r_group");
                TempPlu = rec.getString("r_plucode");
                TempName = rec.getString("r_pname");
                do {
                    if (!TempPlu.equals(rec.getString("r_plucode"))) {
                        GroupRec = new PluRec();
                        GroupRec.MacNo1 = "";
                        GroupRec.MacNo2 = "";
                        GroupRec.Cashier1 = "";
                        GroupRec.Cashier2 = "";
                        GroupRec.Group1 = Group1;
                        GroupRec.Group2 = Group1;
                        GroupRec.Plu1 = Plu1;
                        GroupRec.Plu2 = Plu2;
                        GroupRec.GroupCode = TempGroup;
                        GroupRec.GroupName = PUtility.SeekGroupName(TempGroup);
                        GroupRec.PCode = TempPlu;
                        GroupRec.PName = TempName;

                        GroupRec.E_Qty = GroupRec.E_Qty + SumEQty;
                        GroupRec.E_Amt = GroupRec.E_Amt + SumEAmt;


                        GroupRec.T_Qty = GroupRec.T_Qty + SumTQty;
                        GroupRec.T_Amt = GroupRec.T_Amt + SumTAmt;

                        GroupRec.S_Qty = GroupRec.S_Qty + SumSQty;
                        if (ArraySize == 0) {
                            GArray[ArraySize] = GroupRec;
                            ArraySize = GArray.length;
                        } else {
                            GArray = PUtility.addPluArray(GArray);
                            ArraySize = GArray.length;
                            GArray[ArraySize - 1] = GroupRec;
                        }
                        TempGroup = rec.getString("r_group");
                        TempPlu = rec.getString("r_plucode");
                        TempName = rec.getString("r_pname");
                        SumEQty = 0.0;
                        SumEAmt = 0.0;
                        SumTQty = 0.0;
                        SumTAmt = 0.0;
                        SumSQty = 0.0;
                    }
                    if (rec.getString("b_chargegroup").equals("1")) {
                        SumEQty = SumEQty + rec.getDouble("r_quan");
                        SumEAmt = SumEAmt + rec.getDouble("r_nettotal");
                    }
                    if (rec.getString("b_chargegroup").equals("2")) {
                        SumTQty = SumTQty + rec.getDouble("r_quan");
                        SumTAmt = SumTAmt + rec.getDouble("r_nettotal");
                    }
                    SumSQty = SumSQty+rec.getDouble("r_quan");
                } while (rec.next());
                if (SumSQty > 0) {
                    GroupRec = new PluRec();
                    GroupRec.MacNo1 = "";
                    GroupRec.MacNo2 = "";
                    GroupRec.Cashier1 = "";
                    GroupRec.Cashier2 = "";
                    GroupRec.Group1 = Group1;
                    GroupRec.Group2 = Group1;
                    GroupRec.Plu1 = Plu1;
                    GroupRec.Plu2 = Plu2;
                    GroupRec.GroupCode = TempGroup;
                    GroupRec.GroupName = PUtility.SeekGroupName(TempGroup);
                    GroupRec.PCode = TempPlu;
                    GroupRec.PName = TempName;

                    GroupRec.E_Qty = GroupRec.E_Qty + SumEQty;
                    GroupRec.E_Amt = GroupRec.E_Amt + SumEAmt;


                    GroupRec.T_Qty = GroupRec.T_Qty + SumTQty;
                    GroupRec.T_Amt = GroupRec.T_Amt + SumTAmt;



                    GroupRec.S_Qty = GroupRec.S_Qty + SumSQty;
                    if (ArraySize == 0) {
                        GArray[ArraySize] = GroupRec;
                        ArraySize = GArray.length;
                    } else {
                        GArray = PUtility.addPluArray(GArray);
                        ArraySize = GArray.length;
                        GArray[ArraySize - 1] = GroupRec;
                    }
                }
            }

            rec.close();
            stmt.close();
        } catch (SQLException e) {
            PUtility.showError(e.getMessage());
        }
        prn.PrintCharge3(GArray);
        txtGroup1.requestFocus();
    }
